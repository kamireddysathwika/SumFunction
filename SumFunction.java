public class SumFunction {
 public static void main(String[] args) {
        int num1 = 15;
        int num2 = 25;
        int result = sum(num1, num2);
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + result);
    }
    public static int sum(int a, int b) {
        return a + b;
    }
}
